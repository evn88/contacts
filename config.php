function accessIp()
{
    $accessIp = array('10.77.101.48', '10.77.101.155', '10.77.101.107');
    return in_array($_SERVER["REMOTE_ADDR"], $accessIp);
}

function countPeople()
{
    global $mysqli;
    $query = $mysqli->query("SELECT count(*) FROM people WHERE `people`.`hide` = 0");
    $result = $query->fetch_array();
    return $result[0];
}

function page_referesh()
{
